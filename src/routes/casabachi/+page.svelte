<h1>COSA È CASA BACHI?</h1>
<br />
<p>
	Casa Bachi è lo spazio in cui convergono gli interessi artistici e culturali dell’associazione
	Bachi da Setola, un aggregatore sociale e organizzatore di eventi musicali, teatrali e di progetti
	di scambio all’interno della comunità europea. La casa è un vivo spazio di dialogo con la
	comunità, offre accoglienza e ristoro ma anche possibilità di crescita con workshop, laboratori,
	residenze, concerti, presentazioni di libri, occasioni di dibattito e di svago. Le attività sono
	rivolte all’intera comunità locale, da bambinə a ragazzə ad adultə, senza tralasciare la
	componente del turismo stagionale.
</p>
